// Build sector imagery as AVIF + WebP + JPG fallback.
// Closes issue #15.
//
// Run once locally:
//   npm install --no-save sharp
//   node scripts/build-sector-images.mjs
//
// Output:
//   assets/sectors/<sector>.{avif,webp,jpg}
//   polish/picture-template.html — paste this <picture> snippet into
//     sectorImg() in index.html

import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import https from 'node:https';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const OUT = path.join(ROOT, 'assets', 'sectors');
const TEMPLATE_OUT = path.join(ROOT, 'polish', 'picture-template.html');

// Map sectors → Unsplash photo URLs. Extracted from the sectorImg()
// function in index.html. Update this list when sectors change.
const SECTORS = {
  'ict':                'https://images.unsplash.com/photo-1518770660439-4636190af475?w=1200',
  'healthcare':         'https://images.unsplash.com/photo-1576091160550-2173dba999ef?w=1200',
  'energy':             'https://images.unsplash.com/photo-1466611653911-95081537e5b7?w=1200',
  'agriculture':        'https://images.unsplash.com/photo-1500382017468-9049fed747ef?w=1200',
  'education':          'https://images.unsplash.com/photo-1503676260728-1c00da094a0b?w=1200',
  'finance':            'https://images.unsplash.com/photo-1444653614773-995cb1ef9efa?w=1200',
  'tourism':            'https://images.unsplash.com/photo-1564506474071-fd0c4f49abc5?w=1200',
  'mining':             'https://images.unsplash.com/photo-1582719471384-894fbb16e074?w=1200',
  'manufacturing':      'https://images.unsplash.com/photo-1581094794329-c8112a89af12?w=1200',
  'transport':          'https://images.unsplash.com/photo-1494412519320-aa613dfb7738?w=1200',
  'real-estate':        'https://images.unsplash.com/photo-1486325212027-8081e485255e?w=1200',
  'sports':             'https://images.unsplash.com/photo-1461896836934-ffe607ba8211?w=1200',
  'entertainment':      'https://images.unsplash.com/photo-1493711662062-fa541adb3fc8?w=1200',
  'innovation':         'https://images.unsplash.com/photo-1556761175-5973dc0f32e7?w=1200',
  'environment':        'https://images.unsplash.com/photo-1441974231531-c6227db76b6e?w=1200'
};

function fetch(url) {
  return new Promise((resolve, reject) => {
    https.get(url, (res) => {
      if (res.statusCode >= 300 && res.statusCode < 400 && res.headers.location) {
        return fetch(res.headers.location).then(resolve, reject);
      }
      if (res.statusCode !== 200) {
        return reject(new Error('HTTP ' + res.statusCode + ' for ' + url));
      }
      const chunks = [];
      res.on('data', (c) => chunks.push(c));
      res.on('end', () => resolve(Buffer.concat(chunks)));
    }).on('error', reject);
  });
}

async function main() {
  let sharp;
  try {
    sharp = (await import('sharp')).default;
  } catch (e) {
    console.error('sharp not installed. Run: npm install --no-save sharp');
    process.exit(1);
  }

  await fs.mkdir(OUT, { recursive: true });

  for (const [sector, url] of Object.entries(SECTORS)) {
    process.stdout.write(`${sector.padEnd(15)} `);
    const buf = await fetch(url);
    const img = sharp(buf).resize(1200, 800, { fit: 'cover', position: 'attention' });

    await Promise.all([
      img.clone().avif({ quality: 60, effort: 6 }).toFile(path.join(OUT, `${sector}.avif`)),
      img.clone().webp({ quality: 75, effort: 5 }).toFile(path.join(OUT, `${sector}.webp`)),
      img.clone().jpeg({ quality: 80, progressive: true, mozjpeg: true }).toFile(path.join(OUT, `${sector}.jpg`))
    ]);

    const sizes = await Promise.all([
      fs.stat(path.join(OUT, `${sector}.avif`)),
      fs.stat(path.join(OUT, `${sector}.webp`)),
      fs.stat(path.join(OUT, `${sector}.jpg`))
    ]);
    console.log(`avif ${(sizes[0].size/1024).toFixed(0)}KB · webp ${(sizes[1].size/1024).toFixed(0)}KB · jpg ${(sizes[2].size/1024).toFixed(0)}KB`);
  }

  // Emit the <picture> template the SPA can adopt.
  await fs.mkdir(path.dirname(TEMPLATE_OUT), { recursive: true });
  const template = `<!-- Generated by scripts/build-sector-images.mjs.
     Paste this into sectorImg() in index.html so each opportunity card
     ships AVIF first, WebP next, JPG fallback. The 'sectorKey' template
     placeholder should match the JSON key in assets/sectors/. -->
<picture>
  <source type="image/avif" srcset="assets/sectors/\${sectorKey}.avif">
  <source type="image/webp" srcset="assets/sectors/\${sectorKey}.webp">
  <img src="assets/sectors/\${sectorKey}.jpg"
       alt=""
       loading="lazy"
       width="1200" height="800"
       onerror="this.style.display='none';this.parentNode.classList.add('opp-card-image-fallback');">
</picture>
`;
  await fs.writeFile(TEMPLATE_OUT, template);
  console.log('\nTemplate written to ' + path.relative(ROOT, TEMPLATE_OUT));
}

main().catch((e) => { console.error(e); process.exit(1); });
